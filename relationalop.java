public class relationalop {

    public static void main(String[] args) {
        int p = 15, q = 40;
        System.out.println(p == q);// false
        System.out.println(p != q);// true
        System.out.println(p > q);// false
        System.out.println(p < q);// true
        System.out.println(p <= q);// false
        System.out.println(p >= q);// false
    }
}